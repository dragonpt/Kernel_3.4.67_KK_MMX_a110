const exposure_lut_struct *get_ae_lut(ae_lut_info_struct info)
{
  kal_uint32 idxHz = 0;
  kal_uint32 idxVideo = 0;
  kal_int16  idxOffset = 0;

  const exposure_lut_struct *pAeLut = NULL;

  if (info.band==CAM_BANDING_60HZ) idxHz = 1;
  if (info.videomode==KAL_TRUE)
   {
      idxVideo = 1;
      if (CAM_NIGHTSCENE == info.dscmode)
      {
         if (MPEG4_30fps_mode == KAL_TRUE)
         {
            if(info.band==CAM_BANDING_60HZ)
            	pAeLut = AE_NIGHT_60HZ_VIDEO_QCIF;
            else
              pAeLut = AE_NIGHT_50HZ_VIDEO_QCIF;
         }
         else
         {
            pAeLut = AE_LUTs[info.dscmode][idxVideo][idxHz];/* video night mode */
         }
      }
      else
      {
      	 if (MPEG4_30fps_mode == KAL_TRUE)
         {
         	   if(info.band==CAM_BANDING_60HZ)
            	pAeLut = AE_AUTO_60HZ_VIDEO_QCIF;
             else
              pAeLut = AE_AUTO_50HZ_VIDEO_QCIF;
         }
         else
         {
            pAeLut = AE_LUTs[info.dscmode][idxVideo][idxHz];
         }
      }
   }
   else
	{
      pAeLut = AE_LUTs[info.dscmode][idxVideo][idxHz];
   }
  ASSERT(pAeLut != NULL);

  // update ae info
  if(p_device_support_info != NULL)
  {
    idxOffset = (device_support_info.ae.minEv-
                 device_support_info_list[info.dscmode][idxVideo]->ae.minEv)
                 /(device_support_info.ae.step);
    idxOffset = AE_INDEX + idxOffset;
    if(idxOffset > 0)
      AE_INDEX = idxOffset;
    else
      AE_INDEX = 0;
  }
  memcpy(&device_support_info,
        device_support_info_list[info.dscmode][idxVideo],
        sizeof(device_info_struct));
  p_device_support_info = &device_support_info;
  return pAeLut;
}

const kal_uint8 *get_iris_lut(ae_lut_info_struct info)
{
  kal_uint32 idxVideo = 0;
  const kal_uint8 *pIrisLut = NULL;

  if (info.videomode==KAL_TRUE) idxVideo = 1;

  pIrisLut = IRIS_LUTs[info.dscmode][idxVideo];
  ASSERT(pIrisLut != NULL);
  return pIrisLut;
}

